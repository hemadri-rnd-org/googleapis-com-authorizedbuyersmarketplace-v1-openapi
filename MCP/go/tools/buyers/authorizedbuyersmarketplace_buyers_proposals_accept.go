func CreateAuthorizedbuyersmarketplace_buyers_proposals_acceptTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_v1_name_accept",
		mcp.WithDescription("Accepts the proposal at the given revision number. If the revision number in the request is behind the latest from the server, an error message will be returned. This call updates the Proposal.state from `BUYER_ACCEPTANCE_REQUESTED` to `FINALIZED`; it has no side effect if the Proposal.state is already `FINALIZED` and throws exception if the Proposal.state is not either `BUYER_ACCEPTANCE_REQUESTED` or `FINALIZED`. Accepting a proposal means the buyer understands and accepts the Proposal.terms_and_conditions proposed by the seller."),
		mcp.WithString("name", mcp.Required(), mcp.Description("Name of the proposal. Format: `buyers/{accountId}/proposals/{proposalId}`")),
		mcp.WithString("proposalRevision", mcp.Description("Input parameter: The last known client revision number of the proposal.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Authorizedbuyersmarketplace_buyers_proposals_acceptHandler(cfg),
	}
}
